"""
برنامج بسيط لاختبار وظيفة تنسيق أرقام الهواتف
"""
from utils import format_phone_number

# قائمة بأرقام الهواتف للاختبار
test_cases = [
    '0947 312 248',     # رقم بصيغة مع مسافات
    '+963947312248',    # رقم مكتمل مع رمز البلد
    '947312248',        # رقم بدون صفر وبدون رمز بلد
    '09 47 31 22 48',   # رقم بمسافات بين كل رقمين
    '0-947-312-248',    # رقم مع شرطات
    '09473-12248',      # رقم مع شرطة واحدة
    '+963 947 312 248', # رقم مكتمل مع مسافات
    '963 947 312 248',  # رقم مكتمل بدون + مع مسافات
    'سوريا 0947312248', # رقم مع نص
    '0947312248 (سوريا)', # رقم مع نص بين قوسين
    '+9639-47312248',   # رقم به خطأ شائع
    '9639-47312248',    # رقم به خطأ شائع بدون +
]

def main():
    """
    اختبار دالة تنسيق أرقام الهواتف
    """
    print('اختبار وظيفة تنسيق أرقام الهواتف:')
    print('-' * 40)
    
    for phone in test_cases:
        formatted = format_phone_number(phone)
        print(f"{phone} -> {formatted}")

if __name__ == "__main__":
    main()