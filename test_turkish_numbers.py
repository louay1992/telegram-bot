"""
اختبار لدالة تنسيق أرقام الهواتف للأرقام التركية
"""
from utils import format_phone_number

def test_turkish_numbers():
    """
    اختبار دالة تنسيق أرقام الهواتف للأرقام التركية
    """
    turkish_test_cases = [
        # الصيغة الأصلية            # النتيجة المتوقعة
        ("+90 535 123 45 67",       "+905351234567"),      # رقم تركي مع رمز الدولة ومسافات
        ("90 535 123 45 67",        "+905351234567"),      # رقم تركي بدون + وبمسافات
        ("00905351234567",          "+905351234567"),      # رقم تركي بصيغة الاتصال الدولي
        ("0090 535 123 45 67",      "+905351234567"),      # رقم تركي بصيغة الاتصال الدولي ومسافات
        ("0535 123 45 67",          "+905351234567"),      # رقم تركي محلي مع مسافات
        ("05351234567",             "+905351234567"),      # رقم تركي محلي بدون مسافات
        ("0535-123-45-67",          "+905351234567"),      # رقم تركي محلي مع شرطات
        ("535 123 45 67",           "+905351234567"),      # رقم تركي بدون الصفر الأول ومسافات
        ("0090 (535) 123 45 67",    "+905351234567"),      # رقم تركي مع أقواس
        ("تركيا 0535 123 45 67",    "+905351234567"),      # رقم تركي مع نص
    ]
    
    print("\n=== اختبار تنسيق أرقام الهواتف التركية ===")
    print("-" * 50)
    
    for original, expected in turkish_test_cases:
        result = format_phone_number(original)
        status = "✅" if result == expected else "❌"
        print(f"{status} الأصلي: '{original}' | النتيجة: '{result}' | المتوقع: '{expected}'")

if __name__ == "__main__":
    test_turkish_numbers()